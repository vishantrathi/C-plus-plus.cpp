#include<iostream>
using namespace std;
int main(){
    int num =2;
    char character='A';
    bool boolean=true;
    float floatnum=3.14;
    double doublenum=3.14159;

    cout<<sizeof(int) <<" the num is "<<num<<endl;
    cout<<sizeof(char) <<" the character is "<<character<<endl;
    cout<<sizeof(bool) <<" the boolean is "<<boolean<<endl;
    cout<<sizeof(float) <<" the float is "<<floatnum<<endl;
    cout<<sizeof(double) <<" the double is "<<doublenum<<endl;

}